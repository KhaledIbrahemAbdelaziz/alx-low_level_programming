#include "3-calc.h"

/**
 * op_add - The sum of two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: The sum.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - The difference of two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: The difference.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - The multiplication of two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: The multiplication.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - The division of two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: The division.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - The modolus of two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: The modolus.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
